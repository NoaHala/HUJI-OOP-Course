package bricker.brick_strategies;

/**
 * enum for the strategy-factory to define what types of strategies
 * we want to choose from.
 */
public enum StrategyOptions {
    /**
     * all the strategies
     */
    ALL,
    /**
     * only the special strategies
     */
 SPECIAL,
    /**
     * only the special strategies without the double strategy
     */
 SPECIAL_NO_DOUBLE};
